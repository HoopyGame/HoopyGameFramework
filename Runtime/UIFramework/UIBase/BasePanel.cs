namespace HoopyGame.UIF
{
    public abstract class BasePanel<T> : BaseUI where T : IUIDataBase
    {
        public T data;
        public override void OnInitComponent()
        {
            base.OnInitComponent();
            uiType = UIType.Panel;
            data = (T)Data;
        }
    }
}